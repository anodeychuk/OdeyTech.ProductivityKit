namespace OdeyTech.ProductivityKit.Enum
{
  /// <summary>
  /// Enumeration for the different types of button names used in the application.
  /// </summary>
  public enum ButtonName
  {
    /// <summary>
    /// Represents a default or non-specified button.
    /// </summary>
    None,

    /// <summary>
    /// Represents a button used for creating new entries or records.
    /// </summary>
    New,

    /// <summary>
    /// Represents a button used for adding entries or records.
    /// </summary>
    Add,

    /// <summary>
    /// Represents a button used for editing existing entries or records.
    /// </summary>
    Edit,

    /// <summary>
    /// Represents a button used for removing existing entries or records.
    /// </summary>
    Remove,

    /// <summary>
    /// Represents a button used for confirming actions.
    /// </summary>
    Yes,

    /// <summary>
    /// Represents a button used for negating or refusing actions.
    /// </summary>
    No,

    /// <summary>
    /// Represents a button used for cancelling actions or operations.
    /// </summary>
    Cancel,

    /// <summary>
    /// Represents a button used for confirming or accepting actions.
    /// </summary>
    Ok,

    /// <summary>
    /// Represents a button used for saving data, changes, or records.
    /// </summary>
    Save
  }
}